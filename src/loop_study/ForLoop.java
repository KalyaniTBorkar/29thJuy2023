package loop_study;

public class ForLoop {
public static void main(String[] args) {
	//int a = 4;
	for(int table =6; table <=60; table=table+6)
	{
		System.out.println(table);
	}
	System.out.println("*************************************");
	for(int no=1; no<=10; no++)
	{
		System.out.print(no+",");
	}
	System.out.println(" ");
	System.out.println("*************************************");
	for( int a=4 ; a <=40; a=a+4) 
	{
		System.out.print(a+",");
	}
	for(int n = 1; n<=10 ; n++)
	{
		System.out.println("pune");
	}
	for(int c=50; c>=5; c=c-5){
		{
			System.out.println(c);
		}
	}
}
}
